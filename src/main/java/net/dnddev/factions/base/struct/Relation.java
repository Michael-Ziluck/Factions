package net.dnddev.factions.base.struct;

/**
 * The relationships two Factions can have with one another.
 *
 * @author Michael Ziluck
 */
public enum Relation
{

    /**
     * Two factions that have an alliance with one another.
     */
    ALLY,
    /**
     * Two factions that are enemies with one another.
     */
    ENEMY,
    /**
     * Two factions that are indifferent with one another.
     */
    NEUTRAL

}
